const student = {

    Stud_Name: "Bennison",
    Age: 21
};

// accessing the object property
console.log(student.Stud_Name);  // Bennison
console.log(student.Age);        // 21



// Another way to accessing the object property

console.log(student["Stud_Name"]);   //Bennison
console.log(student["Age"]);         // 21